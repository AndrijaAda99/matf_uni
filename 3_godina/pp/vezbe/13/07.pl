% X, Y, Z, i W: 
% X >= 2W
% 3 + Y <= Z
% X - 11W + Y + 11Z <= 100
% X in {1,2,...,10}
% Y in {1,3,5,...,51}
% Z in {10,20,30,...,100}
% W in {1,8,15,22,...,100}

uslov(Vs) :- Vs = [X, Y, Z, W],
             X in 1..10,
             Y in 1..51,
             Z in 1..100,
             W in 1..1000,
             X #>= 2 * W,
             X - 11 * W + Y + 11 * Z #=< 100,
             Y - 1 mod 2 #= 0,
             Z mod 10 #= 0,
             W - 1 mod 7 #= 0,
             label(Vs).
